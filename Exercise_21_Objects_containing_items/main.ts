// Write a program that creates Objects containing these items.

// datatype of automobile
interface automobile {
    brand : string,
    color : string,
    reg_Number : number;
    automatic : boolean
}

// car object
let car : automobile = {
    brand : 'Honda',
    color : 'White',
    reg_Number : 5462,
    automatic : true,
}
// print car object
console.log(car);


// datatype of motorcycle
interface motorcycle {
    brand : string,
    color : string,
    reg_Number : number;
    automatic : boolean
}

// bike object
let bike : motorcycle  = {
    brand : 'Suzuki',
    color : 'Black',
    reg_Number : 8962,
    automatic : false,
}
// print bike object
console.log(bike);












